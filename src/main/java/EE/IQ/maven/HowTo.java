package EE.IQ.maven;

public class HowTo {
    /*
        ### Useful commands:

            $ mvn clean // Clean the target folder


        ### Installing maven



        ### Adding new dependencies

            Just type the dependency name of whatever you want on the internet and look for the dependency block
            to copy and paste on your pom.xml. Or weather you already know what you're looking for, go to
            https://search.maven.org/ or https://central.sonatype.com/?smo=true and search for it.

            After that add the code block that you've chosen to your pom.xml dependencies tag:

            <dependency>
                <groupId>com.mpatric</groupId>
                <artifactId>mp3agic</artifactId>
                <version>0.9.1</version>
            </dependency>

            And

            $ mvn install

            To see the new installed dependency, go to Project > External Libraries > <name.of.your.new.dependency>
    */
}
